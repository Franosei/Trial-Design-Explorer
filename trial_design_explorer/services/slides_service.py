def _slide_cover(prs: Presentation, protocol: ProtocolMetadata,
                 page_num: int, total_pages: int) -> None:
    slide = _add_slide(prs)
    # Full-bleed navy background
    _rect(slide, 0, 0, SLIDE_W, SLIDE_H, fill_rgb=NAVY)
    # Left accent bar
    _rect(slide, 0, 0, Inches(0.15), SLIDE_H, fill_rgb=TEAL)

    _text_box(slide, Inches(0.4), Inches(1.4), Inches(11), Inches(0.6),
              "Protocol Intelligence Report",
              font_size=34, bold=True, color=WHITE)
    _text_box(slide, Inches(0.4), Inches(2.15), Inches(11), Inches(0.5),
              "Clinical Trial Design Benchmarking & Decision-Grade Evidence",
              font_size=15, color=RGBColor(0xB0, 0xC4, 0xD8))

    _rect(slide, Inches(0.4), Inches(2.85), Inches(10.5), Pt(1.5), fill_rgb=TEAL)

    meta_lines = [
        f"Protocol:  {protocol.title or 'Not provided'}",
        f"Condition: {protocol.condition or 'Not provided'}",
        f"Sponsor:   {protocol.sponsor or 'Not provided'}",
        f"Phase:     {protocol.phase or 'Not provided'}  |  Study type: {protocol.study_type or 'Not provided'}",
        f"Generated: {current_utc_timestamp()} UTC",
    ]
    for i, line in enumerate(meta_lines):
        _text_box(slide, Inches(0.4), Inches(3.1) + Inches(i * 0.42),
                  Inches(11), Inches(0.38),
                  line, font_size=11,
                  color=RGBColor(0xD0, 0xE4, 0xF0))

    _text_box(slide, Inches(0.4), Inches(6.9), Inches(12), Inches(0.3),
              "Confidential — prepared for clinical development, trial design, and operational planning review.",
              font_size=8, color=SLATE)
    _footer(slide, page_num, total_pages)


def _slide_executive_summary(prs, protocol, metrics, recommendations,
                               page_num, total_pages) -> None:
    slide = _add_slide(prs)
    _heading_bar(slide, "Executive Summary",
                 "Top-line read on precedent posture, evidence strength, and lead actions")
    _footer(slide, page_num, total_pages)

    c_fit = metrics.get("completed_design_fit_pct", "n/a")
    d_fit = metrics.get("disrupted_design_fit_pct", "n/a")
    posture = metrics.get("precedent_posture", "Incomplete")
    cohort = metrics.get("cohort_size", 0)
    evidence = metrics.get("evidence_strength", "Limited")

    kpis = [
        ("Cohort size", str(cohort), TEAL),
        ("Completed fit", f"{c_fit}%" if isinstance(c_fit, (int, float)) else str(c_fit), TEAL),
        ("Disrupted fit", f"{d_fit}%" if isinstance(d_fit, (int, float)) else str(d_fit), RED),
        ("Evidence", evidence, NAVY),
    ]
    for i, (label, value, color) in enumerate(kpis):
        x = Inches(0.3 + i * 3.25)
        _rect(slide, x, Inches(1.0), Inches(3.0), Inches(1.1), fill_rgb=color)
        _text_box(slide, x + Inches(0.1), Inches(1.05), Inches(2.8), Inches(0.4),
                  label, font_size=9, color=WHITE)
        _text_box(slide, x + Inches(0.1), Inches(1.45), Inches(2.8), Inches(0.55),
                  value, font_size=18, bold=True, color=WHITE)

    _text_box(slide, Inches(0.3), Inches(2.25), Inches(12.5), Inches(0.35),
              f"Overall posture: {posture}", font_size=13, bold=True, color=NAVY)

    bullet_items = []
    if protocol.title:
        bullet_items.append(f"Protocol: {protocol.title}")
    if protocol.condition:
        bullet_items.append(f"Clinical focus: {protocol.condition}")
    bullet_items.append(f"Design fit vs completed precedent: {c_fit}%  |  vs disrupted: {d_fit}%")
    if metrics.get("missing_core_fields"):
        bullet_items.append(f"Specification gaps: {', '.join(metrics['missing_core_fields'])}")
    for rec in (recommendations or [])[:3]:
        priority = rec.get("Priority", "")
        action = rec.get("Recommendation", "")
        bullet_items.append(f"[{priority}] {action}")

    y = Inches(2.7)
    for item in bullet_items[:7]:
        _rect(slide, Inches(0.3), y, Inches(0.06), Inches(0.22), fill_rgb=TEAL)
        _text_box(slide, Inches(0.5), y - Inches(0.02), Inches(12.2), Inches(0.32),
                  item, font_size=10, color=NAVY)
        y += Inches(0.38)


def _slide_posture_gauge(prs, metrics, page_num, total_pages) -> None:
    slide = _add_slide(prs)
    _heading_bar(slide, "Precedent Posture",
                 "How the draft design compares to completed vs disrupted trial precedent")
    _footer(slide, page_num, total_pages)

    gauge_buf = generate_posture_gauge(metrics, width_in=5.5, height_in=3.5)
    _add_image_from_buf(slide, gauge_buf, Inches(0.3), Inches(1.0),
                        Inches(5.5), Inches(3.5))

    right_x = Inches(6.2)
    stats = [
        ("Completed fit", metrics.get("completed_design_fit_pct"), TEAL),
        ("Disrupted fit", metrics.get("disrupted_design_fit_pct"), RED),
        ("Net gap", metrics.get("precedent_gap_pct"), NAVY),
        ("Evidence strength", metrics.get("evidence_strength", "Limited"), NAVY),
        ("Completed studies", metrics.get("completed_cohort_size", 0), TEAL),
        ("Disrupted studies", metrics.get("disrupted_cohort_size", 0), RED),
    ]
    for i, (label, value, color) in enumerate(stats):
        y = Inches(1.1) + Inches(i * 0.75)
        _rect(slide, right_x, y, Inches(1.0), Inches(0.55), fill_rgb=color)
        val_str = f"{value}%" if isinstance(value, float) else str(value)
        _text_box(slide, right_x + Inches(0.05), y + Inches(0.08), Inches(0.9), Inches(0.35),
                  val_str, font_size=14, bold=True, color=WHITE, align=PP_ALIGN.CENTER)
        _text_box(slide, right_x + Inches(1.1), y + Inches(0.1), Inches(5.5), Inches(0.35),
                  label, font_size=11, color=NAVY)


def _slide_radar(prs, metrics, page_num, total_pages) -> None:
    slide = _add_slide(prs)
    _heading_bar(slide, "Design Domain Alignment",
                 "Alignment across 7 design domains — completed vs disrupted precedent")
    _footer(slide, page_num, total_pages)

    buf = generate_radar_chart(metrics, width_in=7, height_in=5.5)
    _add_image_from_buf(slide, buf, Inches(0.2), Inches(0.95), Inches(7.0), Inches(5.5))

    rows = metrics.get("alignment_by_domain", [])
    right_x = Inches(7.6)
    _text_box(slide, right_x, Inches(1.0), Inches(5.5), Inches(0.3),
              "Domain signals at a glance", font_size=10, bold=True, color=NAVY)
    for i, row in enumerate(rows[:7]):
        y = Inches(1.4) + Inches(i * 0.68)
        sig = row.get("Signal", "")
        color = TEAL if "Completed" in sig else (RED if "Disrupted" in sig else SLATE)
        _rect(slide, right_x, y, Inches(5.5), Inches(0.55), fill_rgb=SOFT,
              line_rgb=BORDER, line_width_pt=0.5)
        domain_text = f"{row['Domain']}: {row.get('Protocol Choice', '')} — {sig}"
        _text_box(slide, right_x + Inches(0.08), y + Inches(0.08), Inches(5.3), Inches(0.38),
                  domain_text, font_size=8.5, color=color)


