        "Source trials retrieved from ClinicalTrials.gov registry (API v2).  Trials were classified "
        "as completed (COMPLETED) or disrupted (TERMINATED / SUSPENDED / WITHDRAWN) and used as two "
        "distinct comparator lenses.  Protocol fields were extracted from uploaded documents using "
        "deterministic heuristics and optionally LLM-assisted structured parsing.  All outputs are "
        "decision-support intelligence — they do not replace clinical, statistical, operational, or "
