def _slide_pubmed_evidence(prs, pubmed_articles, page_num, total_pages) -> None:
    slide = _add_slide(prs)
    _heading_bar(slide, "Literature Evidence (PubMed)",
                 "Peer-reviewed studies supporting the design rationale — sourced via NCBI PubMed")
    _footer(slide, page_num, total_pages)

    if not pubmed_articles:
        _text_box(slide, Inches(0.3), Inches(2.0), Inches(12), Inches(0.5),
                  "No PubMed articles were retrieved for this analysis.",
                  font_size=11, color=SLATE)
        return

    rows = []
    for art in pubmed_articles[:8]:
        a = art if isinstance(art, dict) else art.to_dict()
        rows.append({
            "PMID": a.get("pmid", ""),
            "Authors (first)": (a.get("authors", "").split(",")[0] + " et al.")[:30],
            "Year": a.get("year", ""),
            "Journal": a.get("journal", "")[:30],
            "Title": a.get("title", "")[:70],
        })
    df = pd.DataFrame(rows)
    _table_on_slide(slide, df,
                    Inches(0.25), Inches(0.95),
                    Inches(12.8), Inches(5.8),
                    font_size=8)

    _text_box(slide, Inches(0.3), Inches(6.9), Inches(12), Inches(0.3),
              "Source: PubMed / NCBI E-utilities (free access). All citations are auditable and traceable.",
              font_size=7.5, color=SLATE)


