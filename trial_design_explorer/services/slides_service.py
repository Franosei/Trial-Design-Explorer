# ── Colour palette ─────────────────────────────────────────────────────────────
NAVY   = RGBColor(0x15, 0x32, 0x4A)
TEAL   = RGBColor(0x1F, 0x5B, 0x7A)
RED    = RGBColor(0xC0, 0x56, 0x3D)
AMBER  = RGBColor(0xF5, 0xA6, 0x23)
