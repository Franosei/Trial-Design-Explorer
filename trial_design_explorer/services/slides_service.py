SLIDE_H = Inches(7.5)


# ── Helpers ────────────────────────────────────────────────────────────────────

def _rgb_hex(rgb: RGBColor) -> str:
    return f"{rgb[0]:02X}{rgb[1]:02X}{rgb[2]:02X}"


def _add_slide(prs: Presentation, layout_idx: int = 6):
    """Add a blank slide (layout 6 = blank in most default themes)."""
    layout = prs.slide_layouts[min(layout_idx, len(prs.slide_layouts) - 1)]
    return prs.slides.add_slide(layout)


def _rect(slide, left, top, width, height, fill_rgb: Optional[RGBColor] = None,
          line_rgb: Optional[RGBColor] = None, line_width_pt: float = 0.0):
    shape = slide.shapes.add_shape(
        1,  # MSO_SHAPE_TYPE.RECTANGLE
        left, top, width, height,
    )
    shape.line.fill.background()
    if fill_rgb:
        shape.fill.solid()
        shape.fill.fore_color.rgb = fill_rgb
    else:
        shape.fill.background()
    if line_rgb:
        shape.line.color.rgb = line_rgb
        shape.line.width = Pt(line_width_pt)
    else:
        shape.line.fill.background()
    return shape


def _text_box(slide, left, top, width, height, text: str,
              font_size: int = 11, bold: bool = False,
              color: RGBColor = NAVY, align=PP_ALIGN.LEFT,
              wrap: bool = True) -> None:
    txb = slide.shapes.add_textbox(left, top, width, height)
    txb.word_wrap = wrap
    tf = txb.text_frame
    tf.word_wrap = wrap
    para = tf.paragraphs[0]
    para.alignment = align
    run = para.add_run()
    run.text = text
    run.font.size = Pt(font_size)
    run.font.bold = bold
    run.font.color.rgb = color
    run.font.name = "Calibri"


def _heading_bar(slide, title: str, subtitle: str = "") -> None:
    """Navy header bar at the top of a content slide."""
    _rect(slide, Inches(0), Inches(0), SLIDE_W, Inches(0.9), fill_rgb=NAVY)
    _text_box(slide, Inches(0.2), Inches(0.08), Inches(12.5), Inches(0.45),
              title, font_size=20, bold=True, color=WHITE)
    if subtitle:
        _text_box(slide, Inches(0.2), Inches(0.52), Inches(12.5), Inches(0.35),
                  subtitle, font_size=10, color=RGBColor(0xB0, 0xC4, 0xD8))


def _add_image_from_buf(slide, buf: io.BytesIO,
                         left, top, width, height) -> None:
    buf.seek(0)
    slide.shapes.add_picture(buf, left, top, width, height)


def _table_on_slide(slide, df: pd.DataFrame,
                    left, top, width, height,
                    font_size: int = 8) -> None:
    if df.empty:
        return
    rows, cols = len(df) + 1, len(df.columns)
    tbl = slide.shapes.add_table(rows, cols, left, top, width, height).table

    col_w = width // cols
    for ci in range(cols):
        tbl.columns[ci].width = col_w

    # Header
    for ci, col_name in enumerate(df.columns):
        cell = tbl.cell(0, ci)
        cell.text = str(col_name)
        cell.fill.solid()
        cell.fill.fore_color.rgb = NAVY
        p = cell.text_frame.paragraphs[0]
        p.alignment = PP_ALIGN.CENTER
        run = p.runs[0] if p.runs else p.add_run()
        run.font.bold = True
        run.font.color.rgb = WHITE
        run.font.size = Pt(font_size)
        run.font.name = "Calibri"

    # Data rows
    safe_df = df.fillna("").astype(str)
    for ri, row_data in enumerate(safe_df.itertuples(index=False), start=1):
        fill_rgb = SOFT if ri % 2 == 0 else WHITE
        for ci, val in enumerate(row_data):
            cell = tbl.cell(ri, ci)
            cell.text = str(val)[:80]
            cell.fill.solid()
            cell.fill.fore_color.rgb = fill_rgb
            p = cell.text_frame.paragraphs[0]
            p.alignment = PP_ALIGN.LEFT
            run = p.runs[0] if p.runs else p.add_run()
            run.font.size = Pt(font_size)
            run.font.color.rgb = NAVY
            run.font.name = "Calibri"


def _footer(slide, page_num: int, total_pages: int) -> None:
    _text_box(
        slide,
        Inches(0.2), Inches(7.2), Inches(9), Inches(0.25),
        "Trial Design Explorer | Clinical Trial Planning Intelligence | Confidential draft",
        font_size=7, color=SLATE,
    )
    _text_box(
        slide,
        Inches(12.3), Inches(7.2), Inches(1.0), Inches(0.25),
        f"{page_num} / {total_pages}",
        font_size=7, color=SLATE, align=PP_ALIGN.RIGHT,
    )


